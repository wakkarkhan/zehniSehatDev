				let router = this.$router.resolve({path: '/pharmacyadmin/index'});
				window.open(router.href, '_blank');
			}
		},
		logoutPatient(){
			this.$localStorage.remove('token');
			this.$localStorage.remove('user');
			this.$localStorage.remove('role');
			this.$router.push('/login');
		}
	},
